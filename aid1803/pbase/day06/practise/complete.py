# 2.完全数
# 1+2+3 = 6(6为完全数)
# 1,2,3都为6的因数（能被一个数x整除的数为y，则y为x因数
# 1*6 = 6
# 2*3 = 6
# 完全数是指除自身以外的所有的因数之和相加等于自身的数，求4-5个完全数并打印
# 答案：6 28 496 8128

for i in range(1,10000):
    sum = 1
    for y in range(2,int(i**(1/2))):
        if i % y == 0:
            sum += y + i/y
            
    if sum == i:
        print (i) 
