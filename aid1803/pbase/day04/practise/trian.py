# 打印三角形，输入n表示三角形的高度和宽度，打印各个方向的三角形
# *
# **
# ***
# ****

#    *
#   **
#  ***
# ****

# ****
#  ***
#   **
#    *


# ****
# ***
# **
# *


n = int(input('请输入三角形的高度和宽度: '))
i = 1
s1 = ''
s2 = ''
s3 = ''
s4 = ''
while i <= n:
    s1 += i*'*' + '\n'
    s2 += (n-i) * ' ' + i*'*' + '\n'
    s3 += (i-1) * ' ' + (n - i + 1)*'*' + '\n'
    s4 += (n - i + 1)*'*'+ '\n'
    i += 1
print(s1)
print(s2)
print(s3)
print(s4)
